/*Crea un array de palabras y ordénalo alfabéticamente usando sort*/

// Crear el array de palabras
const palabras = ["manzana", "banana", "pera", "kiwi", "naranja"];

// Usar sort para ordenar alfabéticamente
palabras.sort();

console.log(palabras); // ["banana", "kiwi", "manzana", "naranja", "pera"]
